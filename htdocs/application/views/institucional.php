                <span class="title">Nuestra historia</span>
                <p>Hasta el 18 de agosto de 1995 la Cooperativa Agrícola de Ramallo contaba con la sección almacén y tienda en su casa central ubicada en la localidad de Villa Ramallo, y con una sucursal en El Paraíso.</p> 
           <p>A los efectos de otorgarle mayor eficiencia a la gestión del emprendimiento y permitir un control más directo, funcionarios y directivos de la entidad promocionaron la constitución de una mutual. La idea era poder alcanzar un mayor desarrollo propio en el rubro, sin perder la afinidad con la cooperativa, pero manejada por una comisión directiva. Así bajo las siglas de MUCOOP comenzó una nueva etapa.</p>
          <p>Muy pronto se vieron los logros, y de a poco el crecimiento fue afirmándose, hasta tal punto que hoy  la mutual cuenta  con su casa central y seis sucursales ubicadas cada una de ellas en zonas de influencia  donde opera la Cooperativa Agrícola, para brindarle servicio a sus asociados y adherentes en general.
 Actualmente  cuenta con más de 7.500 socios y adherentes, los cuales hicieron un aporte fundamental para que fuera posible este crecimiento.</p>
